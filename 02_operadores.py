###Operadores###

print("Operadores aritméticos")

print(3+1) #suma
print(3-1) #resta
print(3*2) #multiplicación
print(3/2) #división
print(3//2) #división entera
print(10%2) #modulo
print(3**3) #exponenciación

print(3*9-2+3/2**2) #jerarquía de operadores

print("Hola " + "Mundo") #concatenación de cadenas de texto, pero no funciona con otros tipos de operadores
print("Hola " + str(5)) #forzar el tipo de dato con la función str()
#print("Hola " + 5) #no se puede concatenar una cadena de texto con un entero
print("Hola mundo \n"* 5) #repetición de cadenas de texto

###Operadores de comparación###
print(5==4) #igualdad
print(5!=4) #desigualdad
print(5<4) #menor que
print(5>4) #mayor que
print(5<=4) #menor o igual que
print(5>=4) #mayor o igual que
print("Comparaciones con cadena de texto")
print("hola" == "hola") # compara por el orden alfabético
print("hola" != "Python") #
print("hola" > "Python") #
print("hola" <= "Python") #
